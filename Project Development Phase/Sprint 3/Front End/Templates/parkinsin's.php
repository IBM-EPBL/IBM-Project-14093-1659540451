                Parkinson’s disease (PD) is one of the most common neurodegenerative diseases with a prevalence rate of 1% in the population above 60 years old, affecting 1 - 2 per 1000 people.
                The estimated global population affected by PD has more than doubled from 1990 to 2016 (from 2.5 million to 6.1 million), which is a result of increased number of elderly people and age-standardized prevalence rates. 
                PD is a progressive neurological disorder associated with motor and non-motor features which comprises multiple aspects of movements, including planning, initiation and execution. The diagnosis of PD is traditionally based on motor symptoms. 
                Despite the establishment of cardinal signs of PD in clinical assessments, most of the rating scales used in the evaluation of disease severity have not been fully evaluated and validated. 
